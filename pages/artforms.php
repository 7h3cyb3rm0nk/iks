    <script>
        // $(document).ready(function() { ... }): This is a jQuery function that runs when the DOM is fully loaded and ready to be manipulated. It ensures that the code inside it will execute only after the DOM has been fully loaded.
        $(document).ready(function() {
            $('#selectionform').submit(function(event) {
                // prevents the default behavior of form submission which is reloading the page
                event.preventDefault();
                // value from the form element having the id search-input is fetched 
                var info = $('#search-input').val().trim();
                if (/^\d+$/.test(info)) {
                    $('#search-input').val('');
                    $('#search-input').attr('placeholder', 'Enter data consisting of alphabetic characters');
                    $('#search-result-section').remove();
                    return false;
                }
                // if the info is not an empty string the if section will work
                if (info !== "") {
                    console.log(info);
                    $.ajax({
                        url: '../api/artformsdescription.php',
                        type: 'GET',
                        data: {
                            'query': info
                        },
                        success: function(data) {
                            if (data !== "") {
                                console.log(data);
                                $('#search-result-section').remove();
                                $('body').append(data);
                            }
                        },
                        error: function(xhr, status, error) {
                            console.error(error);
                        }
                    });
                } else {
                    // the else section will work when the input field is an empty string
                    // Update the placeholder text of the input field
                    $('#search-input').attr('placeholder', 'Input is empty. Please enter a valid search query.');
                    // Set the text color of the placeholder
                    $('#search-result-section').remove();
                }
            });
        });
        // artforms suggestions
        $('#search-input').keyup(function() { // Use keyup event for real-time suggestions
            var suggestion = $(this).val();
            // if the entered input is digits suggestionns won't be shown
            if (/^\d+$/.test(suggestion)) {
                $('#artformsuggestion').html('');
                $('#artformsuggestion').hide();
                return false;
            }
            if (suggestion !== "") {
                $.ajax({
                    url: '../api/artformSuggestion.php',
                    type: 'GET',
                    data: {
                        'query': suggestion
                    },
                    success: function(data) {
                        console.log(data);
                        $('#artformsuggestion').html(data); // Replace the content of #artformsuggestion
                    },
                    error: function(xhr, status, error) {
                        console.error(error);
                    }
                });
            } else {
                $('#artformsuggestion').html('');
                $('#artformsuggestion').hide();
            }
        });
    </script>

